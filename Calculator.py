num1 = input("enter your first number: ")
num2 = input("enter your second number: ")
op = input("+,-,*,/: ")
if op == "+":
    print (float(num1) + float(num2))
elif op == '-':
    print (float(num1) + float(num2))
elif op == '*':
    print (float(num1) * float(num2))
elif op == "/":
    print (float(num1) / float(num2))
else :
    print("cant divide with 0")

